package day0315.opertor;

public class OperatorEx8 {

	public static void main(String[] args) {
		int i = 10;
		System.out.println(i+10);
		System.out.println("i : "+ i);
		
		System.out.println(i+=10);
		System.out.println("i : "+ i);
		
		System.out.println(i=i+10);
		System.out.println("i : "+ i);
	}

}
