package day0318.forex;

public class MultiForEx1 {

	public static void main(String[] args) {
		for (int i = 0; i<3;i++) {
			for (int j = 0; j< 2;j++) {
				System.out.println(i+" : "+j);
			}//inner for
			System.out.println("-----------");
		}//outer for
		System.out.println("program end");
		
		
	}//main
}//class