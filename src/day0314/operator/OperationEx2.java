package day0314.operator;

public class OperationEx2 {

	public static void main(String[] args) {
		//증감연산자
		
		int i=10;
		
		System.out.println(i+1);
		System.out.println(i++);
		
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(i);
		
		System.out.println(i--);
		System.out.println(i);

	}

}
